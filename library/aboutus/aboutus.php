<h3 style="text-align:center">About Us</h3>
<p>
The central library of NIT Sikkim is designated as Knowledge and Information Centre(KIC). Presently the centre is headed by Mr. Swapan Manna, Knowledge and Information Officer (KIO) and looked after by FIKILE.
</p>
<p> 
The KIC supports the teaching and research programmes of the Institute and provides facilities for general reading and disseminates information according to the requirement of the users. It is housed in a separate building. The open access system is observed in the KIC.  It has text books, reference books, encyclopaedias, dictionaries, Videos, CDs/DVDs, Journals/Magazines, Local and National newspapers, e-books, Bibliographic and full text databases, NPTEL videos, E-book reader. KIC reading room is open from 10am to 8pm every day. Photocopy, Printing and scanning facilities are available for users. Wi-Fi connections are recent additions to the KIC. Library Automation Software (LIBSYS 7) implemented with barcode facility. Science Direct, IEL, SciFinder, Encyclopaedia Britannica, Pearson E-books (Selected title), Kindle e-book reader and DELNET access are available. Web OPAC is implementing shortly.
</p>
